
package punto2;


public class Operaciones extends Aabstracta {
    


    @Override
    public double suma(double a, double b) {
        return a+b;
    }

    @Override
    public int resta(int a, int b) {
        return a-b;
    }

    @Override
    public double multiplicaion(double a, double b) {
        return a*b;
    }

    @Override
    public double division(double a, double b) {
        return a/b;
    }
    
}
